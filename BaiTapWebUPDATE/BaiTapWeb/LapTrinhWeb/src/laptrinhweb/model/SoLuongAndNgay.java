package laptrinhweb.model;

public class SoLuongAndNgay {
	private int soLuong;
	private String ngay;
	
	public SoLuongAndNgay(int soLuong, String ngay) {
		this.soLuong = soLuong;
		this.ngay = ngay;
	}
	public int getSoLuong() {
		return soLuong;
	}
	public void setSoLuong(int soLuong) {
		this.soLuong = soLuong;
	}
	public String getNgay() {
		return ngay;
	}
	public void setNgay(String ngay) {
		this.ngay = ngay;
	}
	
}
